<template>
  <div class="dashboard">
    <header class="site-header">
      <h1 class="home-link" @click="$router.push('/')">Authly</h1>
      <nav>
        <div class="greeting">
          {{ greetingMessage }}, {{ loggedInUsername }}!
        </div>
        <button class="logout-button" @click="logout">Logout</button>
      </nav>
    </header>

    <div class="main-section">
      <aside class="sidebar">
        <div class="user-info">
          <img class="user-avatar" :src="userAvatar" alt="User Avatar" />
          <h3 class="username">{{ loggedInUsername }}</h3>
          <p class="expires">Expires: {{ expirationDays }} days</p>
          <button class="plan-button">{{ userPlan.toUpperCase() }} PLAN</button>
        </div>
        <ul>
          <li 
              @click="navigate('Licenses')" 
              :class="{ active: currentPage === 'Licenses' }" 
              class="menu-item tab-button"
          >
              <img src="../assets/svg/key-svgrepo-com.svg" alt="Licenses Icon" class="icon" />
              Licenses
          </li>
        </ul>
      </aside>
      
      <section class="content">
        <transition name="fade-slide">
          <div v-if="currentPage === 'Licenses'" class="licenses-tab">
            <div class="licenses-header">
              <h2 class="licenses-title">Licenses</h2>
              <button class="btn-primary" @click="showCreateLicenseModal = true">Create Licenses</button>
            </div>

            <div class="licenses-panel">
              <table class="licenses-table">
                <thead>
                  <tr>
                    <th>Key</th>
                    <th>Creation Date</th>
                    <th>Generated By</th>
                    <th>Duration</th>
                    <th>Type</th>
                    <th>Note</th>
                    <th>Status</th>
                    <th>Actions</th>
                  </tr>
                </thead>
                <tbody>
                  <tr v-for="license in licenses" :key="license.license_key">
                    <td>{{ license.license_key }}</td>
                    <td>{{ formatDate(license.creation_date) }}</td>
                    <td>{{ license.generated_by }}</td>
                    <td>{{ license.duration }}</td>
                    <td>{{ license.type }}</td>
                    <td>{{ license.note }}</td>
                    <td class="status"><span :class="license.status">{{ license.status }}</span></td>
                    <td>
                      <button class="btn-action edit" @click="editLicense(license)">Edit</button>
                      <button v-if="license.status === 'banned'" class="btn-action unban" @click="unbanLicense(license)">Unban</button>
                      <button v-else class="btn-action ban" @click="banLicense(license)">Ban</button>
                      <button class="btn-action delete" @click="deleteLicense(license)">Delete</button>
                    </td>
                  </tr>
                </tbody>
              </table>
            </div>
          </div>
        </transition>
      </section>
    </div>

    <div v-if="showCreateLicenseModal" class="modal-overlay">
      <div class="modal">
        <h3>Create Licenses</h3>
        <label>
          License Name:
          <input type="text" v-model="newLicenseName" placeholder="##-#############" />
        </label>
        <label>
          License Amount:
          <input type="number" v-model="newLicenseAmount" min="1" />
        </label>
        <label>
          License Note:
          <input type="text" v-model="newLicenseNote" placeholder="Optional note" />
        </label>
        <label>
          License Type:
          <select v-model="newLicenseType">
            <option value="Day">Day</option>
            <option value="Week">Week</option>
            <option value="Month">Month</option>
            <option value="Year">Year</option>
          </select>
        </label>
        <label>
          License Duration:
          <input type="number" v-model="newLicenseDuration" min="1" />
        </label>
        <div class="modal-actions">
          <button class="btn-primary" @click="createLicenses">Create</button>
          <button class="btn-secondary" @click="showCreateLicenseModal = false">Cancel</button>
        </div>
      </div>
    </div>
  </div>
</template>

<script>
import CryptoJS from "crypto-js";

export default {
  data() {
    return {
      loggedInUsername: "",
      expirationDays: 0,
      userPlan: "FREE",
      userAvatar: "",
      userSecret: "",
      currentPage: "Licenses",
      licenses: [],
      showCreateLicenseModal: false,
      newLicenseName: "##-#############",
      newLicenseAmount: 1,
      newLicenseNote: "",
      newLicenseType: "Day",
      newLicenseDuration: 1,
    };
  },
  methods: {
    async fetchUserData(username) {
      const response = await fetch(`http://localhost:3000/user/${username}`);
      const data = await response.json();
      this.userPlan = data.plan || "FREE";
      this.userAvatar = data.avatar || "default-avatar.png";
      this.userSecret = data.secret;
      this.expirationDays = Math.max(
        0,
        Math.ceil((new Date(data.license_date) - new Date()) / (1000 * 60 * 60 * 24))
      );
      this.fetchLicenses();
    },
    async fetchLicenses() {
      const response = await fetch(`http://localhost:3000/licenses?user_secret=${this.userSecret}`);
      this.licenses = await response.json();
    },
    async deleteLicense(license) {
      await fetch(`http://localhost:3000/licenses/${license.license_key}`, {
        method: "DELETE",
      });
      alert("License deleted successfully");
      this.fetchLicenses();
    },
    async banLicense(license) {
      await fetch(`http://localhost:3000/licenses/${license.license_key}/ban`, {
        method: "POST",
      });
      alert("License banned successfully");
      this.fetchLicenses();
    },
    async unbanLicense(license) {
      await fetch(`http://localhost:3000/licenses/${license.license_key}/edit`, {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({ status: "active" }),
      });
      alert("License unbanned successfully");
      this.fetchLicenses();
    },
    async editLicense(license) {
      const newDuration = prompt("Enter new duration:", license.duration);
      const newType = prompt("Enter new type (Day/Week/Month/Year):", license.type);
      const newNote = prompt("Enter a new note:", license.note);
      if (newDuration && newType && newNote !== null) {
        await fetch(`http://localhost:3000/licenses/${license.license_key}/edit`, {
          method: "POST",
          headers: { "Content-Type": "application/json" },
          body: JSON.stringify({ duration: newDuration, type: newType, note: newNote }),
        });
        alert("License updated successfully");
        this.fetchLicenses();
      }
    },
    async createLicenses() {
      const licenses = [];
      for (let i = 0; i < this.newLicenseAmount; i++) {
        const randomPart = this.newLicenseName.replace(/#+/g, () =>
          Math.random().toString(36).substring(2, 8).toUpperCase()
        );
        licenses.push({
          user_secret: this.userSecret,
          license_key: randomPart,
          duration: this.newLicenseDuration,
          type: this.newLicenseType,
          note: this.newLicenseNote,
          generated_by: this.loggedInUsername,
        });
      }
      await fetch('http://localhost:3000/licenses', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ licenses }),
      });
      alert("Licenses created successfully");
      this.fetchLicenses();
      this.showCreateLicenseModal = false;
    },
    navigate(page) {
      this.currentPage = page;
    },
    logout() {
      localStorage.removeItem("authlyUser");
      alert("Logged out successfully");
      this.$router.push("/login");
    },
    formatDate(date) {
      const options = { year: "numeric", month: "short", day: "numeric" };
      return new Date(date).toLocaleDateString(undefined, options);
    },
  },
  mounted() {
    const storedUser = localStorage.getItem("authlyUser");
    if (storedUser) {
      const bytes = CryptoJS.AES.decrypt(storedUser, "authly_secret_key");
      const decryptedData = JSON.parse(bytes.toString(CryptoJS.enc.Utf8));
      this.loggedInUsername = decryptedData.username;
      this.fetchUserData(decryptedData.username);
    }
  },
  computed: {
    greetingMessage() {
      const hour = new Date().getHours();
      if (hour < 6) return "Good night";
      if (hour < 12) return "Good morning";
      if (hour < 18) return "Good afternoon";
      return "Good evening";
    },
  },
};
</script>

